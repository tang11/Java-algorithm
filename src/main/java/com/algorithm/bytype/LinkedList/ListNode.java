package com.algorithm.bytype.LinkedList;

/**
 * @author tanglijuan
 * @date 2021/8/30
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
